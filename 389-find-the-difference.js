/*
You are given two strings s and t.
String t is generated by random shuffling string s and then add one more letter at a random position.
Return the letter that was added to t.

Example 1:

Input: s = "abcd", t = "abcde"
Output: "e"
Explanation: 'e' is the letter that was added.

Example 2:

Input: s = "", t = "y"
Output: "y"
*/

/*
O(nlogn) where n is the length of t
1.) split s and t and sort them
2.) iterate across the t array
3.) when the elements don't match, the element of the t array is the added letter --> return that element
*/

var findTheDifference = function(s, t) {
    let arr1 = s.split("").sort();
    let arr2 = t.split("").sort();

    for (let i = 0; i < arr2.length; i++) {
        if (arr1[i] !== arr2[i]) {
            return arr2[i];
        }
    }
};
